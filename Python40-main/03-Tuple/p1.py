# tuple
# we can create tuple using parenthesis and tuple()
# In tuple we maintain insertion order and we allow duplicates
# tuple is immutable
t1 = 10
print(t1, type(t1))

t2 = 10, 20, 30, 40, 50
print(t2, type(t2))

t3 = (10, 20, 30, 30, 50)
print(t3, type(t3))

t4 = tuple([10, 20, 30, 40, 50])
print(t4, type(t4))

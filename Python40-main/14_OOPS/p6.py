# len()
# find the length of list and str and dict
# len() is finding the length of list
l1 = [10, 20, 30, 40, 50]
print(len(l1))  # 5

# len() is finding the length of keys
d1 = {1: 10, 2: 30, 3: 30, 4: 40}
print(len(d1))  # 4

# len() is finding the length of string
s1 = "python"
print(len(s1))  # 6


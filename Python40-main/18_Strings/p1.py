# string : strings are sequence of characters
user_name = "Admin"
print(user_name)  # Admin

# find the length of the string
print(len(user_name))  # 5

# find indexing
print(user_name[0])  # A
print(user_name[1])  # d

print(user_name[-1])  # n
print(user_name[-2])  # i

# find slicing
print(user_name[0:3])  # Adm

# concatenation +
user_name = "system"
user_password = "admin"
print(user_name, user_password)  # system admin
print(user_name+user_password)  # systemadmin

# repetition
print(user_name * 5)

# escape sequences
# \n new line
print("hello\n, user")

# horizontal tab
print('hello\t,user')  # hello	,user

# single quotation mark
print('hello\' user')  # hello' user

# double quotation mark
print('hello\" user')  # hello" user

# backslash
print('hello\\user')  # hello\user

# backspace
print('hello\buser')  # helluser

